// Printing Values>> integer, float and character

#include<stdio.h>
int main(){
    int a = 10;          // Integer value
    printf("%d\n",a);   // printing Integer value

    float b = 10.5;      // Floating Value
    printf("%f\n",b);   // Printing Floating value

    char c = 'C';       // Character Value
    printf("%c",c);     // printing Character value
    

    return 0;
}